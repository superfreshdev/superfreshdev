/* ----------------------------------------------------------------- */
/* Unset | Radios
/* ----------------------------------------------------------------- */

function unset_radios( radios ) {

  for( let i=0; i < radios.length; i++ ) {

      radios[i].checked = false;

  }

}

/* ----------------------------------------------------------------- */
/* Unset | Checkbox
/* ----------------------------------------------------------------- */

function unset_checkbox( checkbox ) {

  checkbox.checked = false;

}
